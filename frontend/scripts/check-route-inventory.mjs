import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const frontendRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const apiRoot = path.join(frontendRoot, "src", "app", "api");
const inventoryPath = path.join(frontendRoot, "route-inventory.json");
const outputPath = path.join(frontendRoot, "..", "docs", "api-route-inventory-2026-04-29.md");

async function listRouteFiles(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(directory, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await listRouteFiles(fullPath)));
    } else if (entry.isFile() && entry.name === "route.ts") {
      files.push(fullPath);
    }
  }

  return files;
}

function routePathFromFile(file) {
  const relative = path.relative(apiRoot, path.dirname(file));
  return `/api/${relative.split(path.sep).join("/")}`;
}

function assertField(entry, field) {
  if (!entry[field] || (Array.isArray(entry[field]) && entry[field].length === 0)) {
    throw new Error(`Route inventory entry ${entry.path} is missing ${field}.`);
  }
}

async function main() {
  const routeFiles = await listRouteFiles(apiRoot);
  const actualRoutes = routeFiles.map(routePathFromFile).sort();
  const inventory = JSON.parse(await readFile(inventoryPath, "utf8"));
  const inventoryRoutes = inventory.map((entry) => entry.path).sort();
  const missing = actualRoutes.filter((route) => !inventoryRoutes.includes(route));
  const stale = inventoryRoutes.filter((route) => !actualRoutes.includes(route));

  if (missing.length > 0 || stale.length > 0) {
    throw new Error(
      [
        missing.length ? `Missing inventory entries: ${missing.join(", ")}` : "",
        stale.length ? `Stale inventory entries: ${stale.join(", ")}` : "",
      ]
        .filter(Boolean)
        .join("\n")
    );
  }

  for (const entry of inventory) {
    for (const field of [
      "methods",
      "owner",
      "auth",
      "csrf",
      "rateLimit",
      "launchCriticality",
      "tests",
    ]) {
      assertField(entry, field);
    }
  }

  const rows = inventory
    .map((entry) =>
      `| ${[
        entry.path,
        entry.methods.join(", "),
        entry.owner,
        entry.auth,
        entry.csrf,
        entry.rateLimit,
        entry.mutatesSupabase ? "yes" : "no",
        entry.launchCriticality,
        entry.tests.join("<br>"),
      ].join(" | ")} |`
    )
    .join("\n");

  await writeFile(
    outputPath,
    [
      "# API Route Inventory",
      "",
      "Generated by `npm run test:repo-contract` / `frontend/scripts/check-route-inventory.mjs`.",
      "",
      "| Route | Methods | Owner | Auth | CSRF / Origin | Rate limit | Mutates Supabase | Criticality | Test Evidence |",
      "|---|---|---|---|---|---|---|---|---|",
      rows,
      "",
    ].join("\n")
  );

  console.log(`Route inventory covers ${inventory.length} API routes.`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
